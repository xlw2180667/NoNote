import Foundation
import UIKit
extension UIColor {
    class func noNoteBlack() -> UIColor {
        return UIColor(red: 29.0/255.0, green: 28.0/255.0, blue: 28.0/255.0, alpha: 1)
        
    }
    
    class func noNoteGreen() -> UIColor {
        return UIColor(red: 55.0/255.0, green: 189.0/255.0, blue: 0.0/255.0, alpha: 1)
        
    }
}
